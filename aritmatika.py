a = 666
b = 666

#tambah
hasil = a + b
print(a,'+',b,'=',hasil)

#kurang
hasil = a - b
print(a,'-',b,'=',hasil)

#kali
hasil = a * b
print(a,'*',b,'=',hasil)

#bagi
hasil = a / b
print(a,'/',b,'=',hasil)

#eksponen(pangkat)
hasil = a % b
print(a,'**',b,'=',hasil)

#modulus 
hasil = a % b
print(a,'%',b,'=',hasil)

#floor division
hasil = a // b
print(a,'//',b,'=',hasil)

#prioritas operasiop predence
x = 4
y = 12
z = 17

hasil = x + y * z
print(x,'+',y,'*',z,'=',hasil)

#kurung
hasil = x + y * z
print('(',x,'+',y,')*',z,'=',hasil)

